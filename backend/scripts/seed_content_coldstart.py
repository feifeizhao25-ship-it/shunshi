    db_path = os.path.join(os.path.dirname(__file__), "..", "data", "shunshi.db")
    if not os.path.exists(db_path):
        # 尝试从当前目录
        db_path = "data/shunshi.db"

    conn = sqlite3.connect(db_path)

    # 检查现有内容
    cur = conn.cursor()
    cur.execute("SELECT COUNT(*) FROM contents")
    before_total = cur.fetchone()[0]
    print(f"插入前总内容数: {before_total}")

    cur.execute("SELECT type, COUNT(*) FROM contents GROUP BY type")
    print("插入前按类型:")
    for r in cur.fetchall():
        print(f"  {r[0]}: {r[1]}")

    # 1. 插入穴位内容
    print(f"\n=== 插入穴位内容 ({len(ACUPOINT_CONTENTS)} 条) ===")
    # 查找现有acupoint最大编号
    cur.execute("SELECT id FROM contents WHERE type='acupoint' ORDER BY id DESC LIMIT 1")
    last = cur.fetchone()
    start_idx = int(last[0].split('-')[1]) + 1 if last else 1
    for i, item in enumerate(ACUPOINT_CONTENTS):
        insert_content(conn, item, start_idx + i, "acupoint")

    # 2. 插入情志内容
    print(f"\n=== 插入情志内容 ({len(EMOTION_CONTENTS)} 条) ===")
    for i, item in enumerate(EMOTION_CONTENTS):
        insert_content(conn, item, i + 1, "emotion")

    # 3. 插入食谱内容
    print(f"\n=== 插入食谱内容 ({len(RECIPE_CONTENTS)} 条) ===")
    cur.execute("SELECT id FROM contents WHERE type='recipe' ORDER BY id DESC LIMIT 1")
    last = cur.fetchone()
    start_idx = int(last[0].split('-')[1]) + 1 if last else 1
    for i, item in enumerate(RECIPE_CONTENTS):
        insert_content(conn, item, start_idx + i, "recipe")

    # 4. 插入节气内容
    print(f"\n=== 插入节气内容 ({len(SOLARTERM_CONTENTS)} 条) ===")
    cur.execute("SELECT id FROM contents WHERE type='solar_term' ORDER BY id DESC LIMIT 1")
    last = cur.fetchone()
    start_idx = int(last[0].split('-')[1]) + 1 if last else 1
    for i, item in enumerate(SOLARTERM_CONTENTS):
        insert_content(conn, item, start_idx + i, "solar_term")

    conn.commit()

    # 验证
    cur.execute("SELECT COUNT(*) FROM contents")
